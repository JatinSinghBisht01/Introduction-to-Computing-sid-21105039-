# Calculating average of three nos taken from user.

no_1 = int(input("enter no 1 : "))
no_2 = int(input("enter no 2 : "))
no_3 = int(input("enter no 3 : "))

sum = (no_1 + no_2 + no_3)
avg = sum/3
print("The sum of nos is ", sum)
print( " The average of nos is ", avg)


                 
